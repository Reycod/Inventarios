    //Metodo que realiza el listado de los datos en un JTable de java
    public void listarAjuste() 
    {
        try 
        {
            DefaultTableModel modelo = (DefaultTableModel) this.tablaListaAjustes.getModel();//creando el modela ára llenar los datos al JTableje
            limpiarTabla(tablaListaAjustes);
            estiloTabla(tablaListaAjustes);
            //realizando la consulta para realizar el listado de los datos
            AjusteDao invDao = new AjusteDao();
            List<Object[]> lista = invDao.listarAjustes();//obteniendo el listado del Kardex por Almacen
            Object[] fila = new Object[modelo.getColumnCount()];
            if (lista.size() > 0) 
            {
                for (int i = 0; i < lista.size(); i++) 
                {
                    fila[0] = lista.get(i)[0];//id
                    fila[1] = lista.get(i)[1];//fecha
                    fila[2] = lista.get(i)[2];//detalle
                    fila[3] = lista.get(i)[3];//responsable
                    
                    fila[4] = lista.get(i)[4];//producto
//                    fila[5] = Double.parseDouble((String) lista.get(i)[5]);//precio
//                    Double subtotal = Double.parseDouble((String) lista.get(i)[2]) * Double.parseDouble((String) lista.get(i)[5]);//subtotal
//                    fila[6] = Operaciones.redondear(subtotal);
                    modelo.addRow(fila);
                }
            } else {
                JOptionPane.showMessageDialog(null, "El almacen seleccionado no tiene productos..!!", "Mensaje..", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al recuperar los datos del Almacen " + e.getMessage(), "Mensaje..", JOptionPane.ERROR_MESSAGE);
        }
    }

    //Metodo que limpia los elementos de la tabla
    public void limpiarTabla(JTable tabla) {
        try 
        {
            DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
            int filas = tabla.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
    }

