    private void tablaListaAjustesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaListaAjustesMousePressed
        // TODO add your handling code here:
        try 
        {
            AjusteDao invDao = new AjusteDao();
            List<Object[]> lista = invDao.listarAjustesCompleto(Integer.parseInt(String.valueOf(tablaListaAjustes.getValueAt(tablaListaAjustes.getSelectedRow(), 0))));
            if (lista.size() > 0) 
            {
                for (int i = 0; i < lista.size(); i++) 
                {
                  lbmAlmacen.setText(String.valueOf(lista.get(i)[9]));//nombre almacen
                  lbmStockInicial.setText(String.valueOf(lista.get(i)[5]));//stock inicial
                  lbmStockAjuste.setText(String.valueOf(lista.get(i)[6]));//
                  lbmPrecioInicial.setText(String.valueOf(lista.get(i)[7]));
                  lbmPrecioAjuste.setText(String.valueOf(lista.get(i)[8]));
                }
            } 
            else 
            {
                JOptionPane.showMessageDialog(null, "No se encuentra el detalle asociado al ajuste..!!", "Mensaje..", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al recuperar los datos del Almacen " + ex.getMessage(), "Mensaje..", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tablaListaAjustesMousePressed

